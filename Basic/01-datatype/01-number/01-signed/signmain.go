package main

import "fmt"

func main() {
	var a int8 = 10
	var b int16 = 134
	var c int8 = -5
	var d int32 = 1000
	var e int64 = -50000

	fmt.Println("Signed Integers:", a) // Output: Signed Integers: 10
	fmt.Println("Signed Integers:", b) // Output: Signed Integers: 134
	fmt.Println("Signed Integers:", c) // Output: Signed Integers: -5
	fmt.Println("Signed Integers:", d) // Output: Signed Integers: 1000
	fmt.Println("Signed Integers:", e) // Output: Signed Integers: -50000

}
